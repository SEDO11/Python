#수식연산
print(1+1)
print(3-2)
print(5*2)
print(6/3)
print(2**3) # 2^3
print(5%3) #나머지
print(5//3) #몫

#비교연산
print(10 > 3) #참
print(4 >= 7) #거짓
print(5 <= 5) #참
print(3 == 3) #둘이 같다, 참
print(4 == 5) #거짓
print(3 + 7 == 10) #참
print(1 != 3) #둘이 같지 않음, 참

#게이트 연산
print(not(1!=3)) # 둘이 같지 않음 하지만 not으로 부정, 거짓
print((3>0) and (3 < 5)) #참
print((3>0) & (3 < 5)) #참
print((3>0) or (3 < 5)) #참
print((3>0) | (3 < 5)) #참
print(5 > 4 > 3) #참
print(5 > 4 > 7) #거짓